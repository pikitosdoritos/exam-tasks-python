# 3. Змінити функцію, щоб вона обробляла ситуацію, коли ключі
#    в обох вхідних словниках дублюються. Правило: при збігу
#    ключів зберігається значення з другого словника.
#
# 4. Додати перевірку типів даних ключів у вхідних словниках.
#    Якщо ключі не є рядками, функція повинна повертати
#    повідомлення про помилку.
# ============================================================
 
print("\n!!! Завдання 2 !!!\n")
 
 
def merge_dicts(dict1, dict2):
    # Перевірка типів ключів обох словників
    for key in dict1:
        if not isinstance(key, str):
            return f"Помилка: ключ '{key}' у першому словнику не є рядком!"
    for key in dict2:
        if not isinstance(key, str):
            return f"Помилка: ключ '{key}' у другому словнику не є рядком!"
 
    # Об'єднання словників (при дублікаті перемагає значення з dict2)
    merged = {}
    merged.update(dict1)
    merged.update(dict2)
 
    # Сортування за ключами в алфавітному порядку
    sorted_merged = dict(sorted(merged.items()))
 
    return sorted_merged
 
 
# ------------------------------------------------------------
# Тест 1: Звичайне об'єднання
# ------------------------------------------------------------
 
print("--- Тест 1: Звичайне об'єднання ---\n")
 
dict_a = {"banana": 3, "apple": 5, "grape": 2}
dict_b = {"mango": 1, "orange": 4, "kiwi": 7}
 
print(f"Словник 1: {dict_a}")
print(f"Словник 2: {dict_b}")
 
result = merge_dicts(dict_a, dict_b)
print(f"Результат: {result}")
 
 
