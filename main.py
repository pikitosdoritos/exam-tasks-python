print(f"Список y після: {remove_duplicates(y)}")
 
 
# ------------------------------------------------------------
# Частина 3: Середнє значення через генератор
# ------------------------------------------------------------
 
def average_generator(lst):
    gen = (num for num in lst)
    total = 0
    count = 0
    for num in gen:
        total += num
        count += 1
    return total / count if count > 0 else 0
 
print("\n--- Частина 3: Середнє значення (через генератор) ---\n")
avg = average_generator(x)
print(f"Середнє значення списку x {x}: {avg:.2f}")
 
 
