# Частина 4: Фільтрація за двома критеріями + піднесення до квадрату
# ------------------------------------------------------------
 
def filter_and_transform(lst, criterion1, criterion2):
    result = []
    for item in lst:
        if criterion1(item) and criterion2(item):
            result.append(item ** 2)
    return result
 
print("\n--- Частина 4: Фільтрація та перетворення ---\n")
 
# Критерій 1: число більше 3
# Критерій 2: число парне
