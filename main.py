import math
 
print("=" * 45)
print("         МОДУЛЬНА КОНТРОЛЬНА РОБОТА")
print("=" * 45)
 
print("\n!!! Завдання 1 !!!\n")

# ------------------------------------------------------------
# Частина 1: Сума чисел у діапазоні [start, end]
# ------------------------------------------------------------
 
print("\n--- Частина 1: Сума чисел у діапазоні ---\n")
 
start = int(input("Введіть початок діапазону (start): "))
end   = int(input("Введіть кінець діапазону (end):   "))
 
if start > end:
    print("\nПомилка: start повинен бути менше або рівний end!")
else:
    total = 0
    for number in range(start, end + 1):
        total += number
    print(f"\nСума чисел від {start} до {end} = {total}")
 
 
# ------------------------------------------------------------
# Частина 2: Факторіал числа n
# ------------------------------------------------------------
 
print("\n--- Частина 2: Факторіал числа ---\n")
 
n = int(input("Введіть ціле число n для обчислення факторіалу: "))
 
if n < 0:
    print("\nПомилка: n повинно бути невід'ємним цілим числом!")
else:
    factorial = 1
    for i in range(1, n + 1):
        factorial *= i
    print(f"\nФакторіал {n}! = {factorial}")
 
 
# ------------------------------------------------------------
# Частина 3: Перевірка числа на простоту
# ------------------------------------------------------------
 
print("\n--- Частина 3: Перевірка простого числа ---\n")
 
n = int(input("Введіть число n для перевірки на простоту: "))
 
if n < 2:
    print(f"\nЧисло {n} не є простим (просте число >= 2)")
else:
    is_prime = True
    for i in range(2, int(math.sqrt(n)) + 1):
        if n % i == 0:
            is_prime = False
            break
 
    if is_prime:
        print(f"\nЧисло {n} є простим")
    else:
        print(f"\nЧисло {n} не є простим")
 
 
