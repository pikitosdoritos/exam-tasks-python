# ------------------------------------------------------------
# Частина 1: Унікальні спільні елементи двох списків
# ------------------------------------------------------------
 
def common_unique(lst1, lst2):
    result = []
    for item in lst1:
        if item in lst2 and item not in result:
            result.append(item)
    return result
 
print("\n--- Частина 1: Спільні унікальні елементи ---\n")
common = common_unique(x, y)
print(f"Спільні унікальні елементи x і y: {common}")
 
 
# ------------------------------------------------------------
# Частина 2: Видалення дублікатів зі збереженням порядку
# ------------------------------------------------------------
 
def remove_duplicates(lst):
    seen = []
    result = []
    for item in lst:
        if item not in seen:
            seen.append(item)
            result.append(item)
    return result
 
print("\n--- Частина 2: Видалення дублікатів ---\n")
print(f"Список x до:    {x}")
print(f"Список x після: {remove_duplicates(x)}")
